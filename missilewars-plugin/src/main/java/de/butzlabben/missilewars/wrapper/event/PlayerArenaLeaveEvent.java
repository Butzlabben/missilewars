package de.butzlabben.missilewars.wrapper.event;

import de.butzlabben.missilewars.game.Game;
import lombok.Getter;
import org.bukkit.entity.Player;
import org.bukkit.event.Cancellable;
import org.bukkit.event.HandlerList;
import org.bukkit.event.player.PlayerEvent;

/**
 * Get's called, when a player has already left an arena
 */
@Getter
public class PlayerArenaLeaveEvent extends PlayerEvent implements Cancellable {

    public final static HandlerList handlers = new HandlerList();
    private final Game game;
    private boolean cancelled;

    public PlayerArenaLeaveEvent(Player who, Game game) {
        super(who);
        this.game = game;
    }

    public static HandlerList getHandlerList() {
        return handlers;
    }

    @Override
    public HandlerList getHandlers() {
        return handlers;
    }

    @Override
    public boolean isCancelled() {
        return cancelled;
    }

    @Override
    public void setCancelled(boolean b) {
        cancelled = b;
    }
}
